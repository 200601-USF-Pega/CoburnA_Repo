package com.revature.monsterfit.models;

public class Weapon {
	private int power;
	private int cost;
	private String name;






public void doDamage() {};

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}


public int getPower() {
	return power;
}


public void setPower(int power) {
	this.power = power;
}


public int getCost() {
	return cost;
}


public void setCost(int cost) {
	this.cost = cost;
}
}


